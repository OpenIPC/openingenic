static int ps5270_probe(struct i2c_client *client,
			const struct i2c_device_id *id)
{
	struct tx_isp_subdev *sd;
	struct tx_isp_video_in *video;
	struct tx_isp_sensor *sensor;

	sensor = (struct tx_isp_sensor *)kzalloc(sizeof(*sensor), GFP_KERNEL);
	if(!sensor){
		ISP_ERROR("Failed to allocate sensor subdev.\n");
		return -ENOMEM;
	}
	memset(sensor, 0 ,sizeof(*sensor));
	/* request mclk of sensor */
	sensor->mclk = clk_get(NULL, "cgu_cim");
	if (IS_ERR(sensor->mclk)) {
		ISP_ERROR("Cannot get sensor input clock cgu_cim\n");
		goto err_get_mclk;
	}
	private_clk_set_rate(sensor->mclk, 24000000);
	private_clk_enable(sensor->mclk);

	if(sensor_resolution == TX_SENSOR_RES_200){
		wsize = &ps5270_win_sizes[0];
		ps5270_attr.total_width = 0xe00 / 2;
		ps5270_attr.total_height = 0x77f + 1;
		ps5270_attr.max_integration_time_native = 0x77f - 2;
		ps5270_attr.integration_time_limit = 0x77f - 2;
		ps5270_attr.max_integration_time = 0x77f - 2;
		ps5270_attr.one_line_expr_in_us = 21;
		ps5270_attr.mipi.clk = 600;
		ps5270_attr.mipi.image_twidth= 1536;
		ps5270_attr.mipi.image_theight= 1536;
	} else if(sensor_resolution == TX_SENSOR_RES_30){
		wsize = &ps5270_win_sizes[1];
		ps5270_attr.total_width = 0xc4e / 2;
		ps5270_attr.total_height = 0x1ef + 1;
		ps5270_attr.max_integration_time_native = 0x1ef - 2;
		ps5270_attr.integration_time_limit = 0x1ef - 2;
		ps5270_attr.max_integration_time = 0x1ef - 2;
		ps5270_attr.one_line_expr_in_us = 19;
		ps5270_attr.mipi.clk = 400;
		ps5270_attr.mipi.image_twidth= 640;
		ps5270_attr.mipi.image_theight= 480;
	} else if(sensor_resolution == TX_SENSOR_RES_100){
		wsize = &ps5270_win_sizes[2];
		ps5270_attr.total_width = 0xc4e / 2;
		ps5270_attr.total_height = 0x4bd + 1;
		ps5270_attr.max_integration_time_native = 0x4bd - 2;
		ps5270_attr.integration_time_limit = 0x4bd - 2;
		ps5270_attr.max_integration_time = 0x4bd - 2;
		ps5270_attr.one_line_expr_in_us = 18;
		ps5270_attr.mipi.clk = 600;
		ps5270_attr.mipi.image_twidth= 1440;
		ps5270_attr.mipi.image_theight= 1080;
	} else {
		pr_debug("%s:%d:Can not support this mode!!!\n", __func__, __LINE__);
	}

	/*
	  convert sensor-gain into isp-gain,
	*/
	ps5270_attr.dbus_type = data_interface;
	ps5270_attr.max_again = 458752;
	ps5270_attr.max_dgain = 0;
	sd = &sensor->sd;
	video = &sensor->video;
	sensor->video.attr = &ps5270_attr;
	sensor->video.vi_max_width = wsize->width;
	sensor->video.vi_max_height = wsize->height;
	sensor->video.mbus.width = wsize->width;
	sensor->video.mbus.height = wsize->height;
	sensor->video.mbus.code = wsize->mbus_code;
	sensor->video.mbus.field = V4L2_FIELD_NONE;
	sensor->video.mbus.colorspace = wsize->colorspace;
	sensor->video.fps = wsize->fps;
	tx_isp_subdev_init(&sensor_platform_device, sd, &ps5270_ops);
	tx_isp_set_subdevdata(sd, client);
	tx_isp_set_subdev_hostdata(sd, sensor);
	private_i2c_set_clientdata(client, sd);

	pr_debug("probe ok ------->ps5270\n");

	return 0;

err_get_mclk:
	kfree(sensor);

	return -1;
}

static int ps5270_remove(struct i2c_client *client)
{
	struct tx_isp_subdev *sd = private_i2c_get_clientdata(client);
	struct tx_isp_sensor *sensor = tx_isp_get_subdev_hostdata(sd);

	if(reset_gpio != -1)
		private_gpio_free(reset_gpio);
	if(pwdn_gpio != -1)
		private_gpio_free(pwdn_gpio);

	private_clk_disable(sensor->mclk);
	private_clk_put(sensor->mclk);
	tx_isp_subdev_deinit(sd);
	kfree(sensor);

	return 0;
}

