static int ps5270_detect(struct tx_isp_subdev *sd, unsigned int *ident)
{
	int ret;
	unsigned char v;
	ret = ps5270_read(sd, 0x00, &v);
	pr_debug("-----%s: %d ret = %d, v = 0x%02x\n", __func__, __LINE__, ret,v);
	if (ret < 0) {
		pr_debug("err: ps5270 write error, ret= %d \n",ret);
		return ret;
	}
	if (v != PS5270_CHIP_ID_H)
		return -ENODEV;
	*ident = v;

	ret = ps5270_read(sd, 0x01, &v);
	pr_debug("-----%s: %d ret = %d, v = 0x%02x\n", __func__, __LINE__, ret,v);
	if (ret < 0)
		return ret;
	if (v != PS5270_CHIP_ID_L)
		return -ENODEV;
	*ident = (*ident << 8) | v;

	return 0;
}

static int ps5270_set_integration_time(struct tx_isp_subdev *sd, int value)
{
	struct tx_isp_sensor *sensor = sd_to_sensor_device(sd);
	int ret = 0;
	unsigned int Cmd_OffNy = 0, Cmd_OffNep, Cmd_OffNe;

	Cmd_OffNy = sensor->video.attr->total_height - value - 1;
	Cmd_OffNep = NEPLS_LB + ((Cmd_OffNy*NEPLS_SCALE)>>8);
	Cmd_OffNep = (Cmd_OffNep > NEPLS_LB)?((Cmd_OffNep < NEPLS_UB)?Cmd_OffNep:NEPLS_UB):NEPLS_LB;
	Cmd_OffNe = NE_NEP_CONST - Cmd_OffNep;
	ret = ps5270_write(sd, 0xef, 0x01);
	ret += ps5270_write(sd, 0x0c, (unsigned char)((Cmd_OffNy & 0xff00) >> 8));
	ret += ps5270_write(sd, 0x0d, (unsigned char)(Cmd_OffNy & 0xff));
	ret += ps5270_write(sd, 0x0e, (unsigned char)((Cmd_OffNe & 0x0f00) >> 8));
	ret += ps5270_write(sd, 0x0f, (unsigned char)(Cmd_OffNe & 0xff));
	ret += ps5270_write(sd, 0x5F, (unsigned char)((Cmd_OffNep & 0x0100) >> 8));
	ret += ps5270_write(sd, 0x60, (unsigned char)(Cmd_OffNep & 0xff));
	ret += ps5270_write(sd, 0x09, 0x01);
	if (ret < 0)
		return ret;

	return 0;
}

static int ps5270_set_analog_gain(struct tx_isp_subdev *sd, int value)
{
	int ret = 0;
	unsigned int gain = value, sghd_patch = 1;
	static unsigned char tmp = 0;
	if (gain > AG_HS_MODE){
		if(tmp == 1)
			sghd_patch = 0;
		tmp = 0;
	} else if(gain < AG_LS_MODE){
		if(tmp == 0)
			sghd_patch = 0;
		tmp = 1;
	}
	if(tmp == 0)
		gain -= 64;		// For 4x ratio
	ret = ps5270_write(sd, 0xef, 0x01);
	ret += ps5270_write(sd, 0x83, (unsigned char)(gain & 0xff));
	ret += ps5270_write(sd, 0x18, (unsigned char)(tmp & 0x01));
	if(g_sns_ver == 0x00)
		ret += ps5270_write(sd, 0x97, sghd_patch);	// For HS/LS switching.
	ret += ps5270_write(sd, 0x09, 0x01);
	if (ret < 0)
		return ret;

	return 0;
}

