	if (len && strncmp(sd->chip.name, reg->name, len))
		return -EINVAL;
	if (!private_capable(CAP_SYS_ADMIN))
		return -EPERM;
	ps5270_write(sd, reg->reg & 0xffff, reg->val & 0xff);

	return 0;
