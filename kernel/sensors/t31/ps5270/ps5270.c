static int ps5270_set_fps(struct tx_isp_subdev *sd, int fps)
{
	struct tx_isp_sensor *sensor = sd_to_sensor_device(sd);
	unsigned int pclk = 0;
	unsigned int hts = 0;
	unsigned int vts = 0;
	unsigned int Cmd_Lpf = 0;
	unsigned int Cur_OffNy = 0;
	unsigned int Cur_ExpLine = 0;
	unsigned char tmp;
	unsigned int newformat = 0; //the format is 24.8
	int ret = 0;
	unsigned int max_fps = SENSOR_OUTPUT_MAX_FPS;

	switch (sensor_max_fps) {
	case TX_SENSOR_MAX_FPS_30:
		pclk = PS5270_SUPPORT_30FPS_MIPI_SCLK;
		max_fps = TX_SENSOR_MAX_FPS_30;
		break;
	case TX_SENSOR_MAX_FPS_110:
		pclk = PS5270_SUPPORT_VGA_SCLK;
		max_fps = TX_SENSOR_MAX_FPS_110;
		break;
	case TX_SENSOR_MAX_FPS_45:
		pclk = PS5270_SUPPORT_45FPS_MIPI_SCLK;
		max_fps = TX_SENSOR_MAX_FPS_45;
		break;
	default:
		ret = -1;
		ISP_ERROR("Now we do not support this framerate!!!\n");
	}

	/* the format of fps is 16/16. for example 25 << 16 | 2, the value is 25/2 fps. */
	newformat = (((fps >> 16) / (fps & 0xffff)) << 8) + ((((fps >> 16) % (fps & 0xffff)) << 8) / (fps & 0xffff));
	if(newformat > (max_fps << 8) || newformat < (SENSOR_OUTPUT_MIN_FPS << 8)) {
		ISP_ERROR("warn: fps(0x%x),new format(0x%x),maxfps(0x%x),minfps(0x%x) no in range\n", fps,newformat,max_fps,SENSOR_OUTPUT_MIN_FPS);
		return -1;
	}

	ret = ps5270_write(sd, 0xef, 0x01);
	if(ret < 0)
		return -1;
	ret = ps5270_read(sd, 0x27, &tmp);
	hts = tmp;
	ret += ps5270_read(sd, 0x28, &tmp);
	if(ret < 0)
		return -1;

	hts = (((hts & 0x1f) << 8) | tmp) >> 1;

	vts = (pclk * (fps & 0xffff) / hts / ((fps & 0xffff0000) >> 16));
	Cmd_Lpf = vts -1;
	ret = ps5270_write(sd, 0xef, 0x01);
	ret += ps5270_write(sd, 0x0b, (unsigned char)(Cmd_Lpf & 0xff));
	ret += ps5270_write(sd, 0x0a, (unsigned char)(Cmd_Lpf >> 8));
	ret += ps5270_write(sd, 0x09, 0x01);
	if(ret < 0){
		pr_debug("err: ps5270_write err\n");
		return ret;
	}
	ret = ps5270_read(sd, 0x0c, &tmp);
	Cur_OffNy = tmp;
	ret += ps5270_read(sd, 0x0d, &tmp);
	if(ret < 0)
		return -1;
	Cur_OffNy = (((Cur_OffNy & 0xff) << 8) | tmp);
	Cur_ExpLine = sensor->video.attr->total_height - Cur_OffNy;

	sensor->video.fps = fps;
	sensor->video.attr->max_integration_time_native = vts - 2;
	sensor->video.attr->integration_time_limit = vts - 2;
	sensor->video.attr->total_height = vts;
	sensor->video.attr->max_integration_time = vts - 2;
	ret = tx_isp_call_subdev_notify(sd, TX_ISP_EVENT_SYNC_SENSOR_ATTR, &sensor->video);

	ret = ps5270_set_integration_time(sd, Cur_ExpLine);
	if(ret < 0)
		return -1;

	return ret;
}

